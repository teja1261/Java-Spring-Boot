package com.modal;

public class AccountMaster {

	private String accountNo;
	private float openingBalance;
	private String accountType;
	private CustomerMaster customerMaster;
	private BranchMaster branchMaster;

	public AccountMaster() {
		super();
	}

	public AccountMaster(String accountNo, float openingBalance, String accountType, CustomerMaster customerMaster) {
		super();
		this.accountNo = accountNo;
		this.openingBalance = openingBalance;
		this.accountType = accountType;
		this.customerMaster = customerMaster;
	}

	public AccountMaster(String accountNo, float openingBalance, String accountType,
			BranchMaster branchMaster) {
		super();
		this.accountNo = accountNo;
		this.openingBalance = openingBalance;
		this.accountType = accountType;
		this.branchMaster = branchMaster;
	}

	public AccountMaster(String accountNo, float openingBalance, String accountType) {
		super();
		this.accountNo = accountNo;
		this.openingBalance = openingBalance;
		this.accountType = accountType;
	}

	public AccountMaster(String accountNo, float openingBalance, String accountType, CustomerMaster customerMaster,
			BranchMaster branchMaster) {
		super();
		this.accountNo = accountNo;
		this.openingBalance = openingBalance;
		this.accountType = accountType;
		this.customerMaster = customerMaster;
		this.branchMaster = branchMaster;
	}

	public String getAccountNo() {
		return accountNo;
	}

	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}

	public float getOpeningBalance() {
		return openingBalance;
	}

	public void setOpeningBalance(float openingBalance) {
		this.openingBalance = openingBalance;
	}

	public String getAccountType() {
		return accountType;
	}

	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}

	public CustomerMaster getCustomerMaster() {
		return customerMaster;
	}

	public void setCustomerMaster(CustomerMaster customerMaster) {
		this.customerMaster = customerMaster;
	}

	public BranchMaster getBankBranchMaster() {
		return branchMaster;
	}

	public void setBankBranchMaster(BranchMaster branchMaster) {
		this.branchMaster = branchMaster;
	}

}
